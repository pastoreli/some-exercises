let valorFornecido = prompt("Digite um número da semana");
        
if(valorFornecido == 1) {
    console.log("Domingo");
} else if (valorFornecido == 2) {
    console.log("Segunda");
} else if (valorFornecido == 3) {
    console.log("Terca");
} else if (valorFornecido == 4) {
    console.log("Quarta");
} else if (valorFornecido == 5) {
    console.log("Quinta");
} else if (valorFornecido == 6) {
    console.log("Sexta");
} else if (valorFornecido == 7) {
    console.log("Sábado");
} else {
    console.log("Valor inválido");
}